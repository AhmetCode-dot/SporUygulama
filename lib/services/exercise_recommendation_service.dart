import 'package:cloud_firestore/cloud_firestore.dart';
import 'package:firebase_storage/firebase_storage.dart';
import '../models/exercise.dart';

class ExerciseRecommendationService {
  final FirebaseFirestore _firestore = FirebaseFirestore.instance;
  final FirebaseStorage _storage = FirebaseStorage.instance;
  final String _collection = 'exercises';

  // Kullanıcının tercihlerine göre egzersiz önerilerini getir
  Future<List<Exercise>> getRecommendedExercises({
    required List<String> bodyRegions,
    required String goal,
    required List<String> equipment,
    required String environment,
    String? experienceLevel,
    int? sessionDurationMin,
    List<String>? preferredBodyRegions,
  }) async {
    try {
      // Firestore'dan egzersizleri getir
      final QuerySnapshot snapshot = await _firestore.collection(_collection).get();
      
      List<Exercise> allExercises = [];
      
      // Her bir dokümanı işle ve gs:// URL'leri dönüştür
      for (final doc in snapshot.docs) {
        final data = doc.data() as Map<String, dynamic>;
        
        // imageUrl'i kontrol et ve gerekirse dönüştür
        String imageUrl = data['imageUrl'] ?? '';
        if (imageUrl.startsWith('gs://')) {
          try {
            final ref = _storage.refFromURL(imageUrl);
            imageUrl = await ref.getDownloadURL();
            // Map'i güncelle
            data['imageUrl'] = imageUrl;
          } catch (e) {
            print('[ExerciseRecommendationService] gs:// imageUrl dönüştürülemedi: $e');
            // Hata durumunda boş bırak, widget placeholder gösterecek
            data['imageUrl'] = '';
          }
        }
        
        // instructionVideoAsset'i kontrol et ve gerekirse dönüştür
        String? videoUrl = data['instructionVideoAsset'] as String?;
        if (videoUrl != null && videoUrl.startsWith('gs://')) {
          try {
            final ref = _storage.refFromURL(videoUrl);
            data['instructionVideoAsset'] = await ref.getDownloadURL();
          } catch (e) {
            print('[ExerciseRecommendationService] gs:// videoUrl dönüştürülemedi: $e');
          }
        }
        
        allExercises.add(Exercise.fromMap(data));
      }

      // Eğer Firestore'da egzersiz yoksa, varsayılan egzersizleri kullan
      if (allExercises.isEmpty) {
        allExercises = _getDefaultExercises();
      }

      // Filtreleme ve skorlama
      List<Exercise> filteredExercises = allExercises.where((exercise) {
        return _isExerciseSuitable(exercise, bodyRegions, goal, equipment, environment);
      }).toList();

      // Skorlama sistemi ile sıralama
      filteredExercises.sort((a, b) {
        int scoreA = _calculateExerciseScore(
          a,
          bodyRegions,
          goal,
          equipment,
          environment,
          experienceLevel: experienceLevel,
          sessionDurationMin: sessionDurationMin,
          preferredBodyRegions: preferredBodyRegions,
        );
        int scoreB = _calculateExerciseScore(
          b,
          bodyRegions,
          goal,
          equipment,
          environment,
          experienceLevel: experienceLevel,
          sessionDurationMin: sessionDurationMin,
          preferredBodyRegions: preferredBodyRegions,
        );
        return scoreB.compareTo(scoreA); // Yüksek skor önce
      });

      return filteredExercises; // Tüm uygun egzersizleri döndür
    } catch (e) {
      // Hata durumunda varsayılan egzersizleri döndür
      return _getDefaultExercises().where((exercise) {
        return _isExerciseSuitable(exercise, bodyRegions, goal, equipment, environment);
      }).toList();
    }
  }

  // Egzersizin uygun olup olmadığını kontrol et
  bool _isExerciseSuitable(Exercise exercise, List<String> bodyRegions, String goal, 
      List<String> equipment, String environment) {
    
    // Vücut bölgesi uyumu
    bool bodyRegionMatch = exercise.bodyRegions.any((region) => bodyRegions.contains(region));
    
    // Hedef uyumu
    bool goalMatch = exercise.goals.contains(goal);
    
    // Ekipman uyumu (egzersizin gerektirdiği ekipmanlardan en az biri kullanıcının ekipmanlarında olmalı)
    bool equipmentMatch = exercise.equipment.any((eq) => 
      equipment.contains(eq) || 
      eq == 'Vücut Ağırlığı ile Çalışıyorum' ||
      equipment.contains('Vücut Ağırlığı ile Çalışıyorum')
    );
    
    // Ortam uyumu
    bool environmentMatch = exercise.environments.contains(environment);

    return bodyRegionMatch && goalMatch && equipmentMatch && environmentMatch;
  }

  // Egzersiz skorunu hesapla (ne kadar uygun olduğunu)
  int _calculateExerciseScore(
    Exercise exercise,
    List<String> bodyRegions,
    String goal,
    List<String> equipment,
    String environment, {
    String? experienceLevel,
    int? sessionDurationMin,
    List<String>? preferredBodyRegions,
  }) {
    int score = 0;

    // Vücut bölgesi skoru
    final int bodyRegionMatches =
        exercise.bodyRegions.where((region) => bodyRegions.contains(region)).length;
    score += bodyRegionMatches * 10;

    // Kullanıcının özellikle seçtiği bölgeler (varsa) için ekstra ağırlık
    if (preferredBodyRegions != null && preferredBodyRegions.isNotEmpty) {
      final int preferredMatches = exercise.bodyRegions
          .where((region) => preferredBodyRegions.contains(region))
          .length;
      score += preferredMatches * 5;
    }

    // Hedef skoru
    if (exercise.goals.contains(goal)) {
      score += 20;
    }

    // Ekipman skoru
    final int equipmentMatches = exercise.equipment
        .where((eq) => equipment.contains(eq) || eq == 'Vücut Ağırlığı ile Çalışıyorum')
        .length;
    score += equipmentMatches * 5;

    // Ortam skoru
    if (exercise.environments.contains(environment)) {
      score += 15;
    }

    // Zorluk skoru – deneyim seviyesine göre
    if (experienceLevel != null && experienceLevel.isNotEmpty) {
      switch (experienceLevel) {
        case 'beginner':
          // Başlangıç için 1–2 ideal, 3 kabul edilebilir, 4–5 daha düşük puan
          if (exercise.difficulty <= 2) {
            score += 10;
          } else if (exercise.difficulty == 3) {
            score += 5;
          } else {
            score -= 5;
          }
          break;
        case 'intermediate':
          // Orta seviye için 2–3 ideal, 1 ve 4 kabul edilebilir
          if (exercise.difficulty == 2 || exercise.difficulty == 3) {
            score += 10;
          } else if (exercise.difficulty == 1 || exercise.difficulty == 4) {
            score += 5;
          }
          break;
        case 'advanced':
          // İleri seviye için 3–5 tercih edilir, çok kolay hareketler az puan
          if (exercise.difficulty >= 3) {
            score += 10;
          } else {
            score -= 5;
          }
          break;
        default:
          // Bilinmeyen değer gelirse eski mantık
          if (exercise.difficulty == 3) {
            score += 5;
          } else if (exercise.difficulty == 2 || exercise.difficulty == 4) {
            score += 3;
          }
      }
    } else {
      // Eski varsayılan mantık – deneyim bilgisi yoksa
      if (exercise.difficulty == 3) {
        score += 5;
      } else if (exercise.difficulty == 2 || exercise.difficulty == 4) {
        score += 3;
      }
    }

    // Seans süresi – kullanıcının hedef süresine yakın egzersizler öne çıkar
    if (sessionDurationMin != null && sessionDurationMin > 0) {
      final int diff = (exercise.duration - sessionDurationMin).abs();
      if (diff <= 5) {
        score += 10;
      } else if (diff <= 10) {
        score += 5;
      } else if (diff >= 20) {
        score -= 5;
      }
    }

    return score;
  }

